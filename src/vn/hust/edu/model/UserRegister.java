package vn.hust.edu.model;

public class UserRegister {
	private String login="";
	private String pass="";
	private String name="";
	private String mail="";
	private String phone="";
	private String address="";
	private String date="";
	private String instutide="";
	private String department="";
	private String classst="";

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}



	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}


	public String getInstutide() {
		return instutide;
	}

	public void setInstutide(String instutide) {
		this.instutide = instutide;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getClassst() {
		return classst;
	}

	public void setClassst(String classst) {
		this.classst = classst;
	}
}
