package vn.hust.edu.model;

public class SubSubject {
	private boolean status;
	private String sub_sub_id;
	private String 	sub_sub_name;
	private String time_start;
	private String time_end;
	public boolean isStatus() {
		return status;
	}
	public void setStatus(boolean status) {
		this.status = status;
	}
	public String getSub_sub_id() {
		return sub_sub_id;
	}
	public void setSub_sub_id(String sub_sub_id) {
		this.sub_sub_id = sub_sub_id;
	}
	public String getSub_sub_name() {
		return sub_sub_name;
	}
	public void setSub_sub_name(String sub_sub_name) {
		this.sub_sub_name = sub_sub_name;
	}
	public String getTime_start() {
		return time_start;
	}
	public void setTime_start(String time_start) {
		this.time_start = time_start;
	}
	public String getTime_end() {
		return time_end;
	}
	public void setTime_end(String time_end) {
		this.time_end = time_end;
	}
	
}
